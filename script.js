function getCurrentLanguage() {
  return localStorage.getItem('siteLanguage') || 'en';
}

function setLanguage(lang) {
  const labels = translations[lang] || translations.en;

  document.documentElement.lang = lang === 'es' ? 'es' : 'en';
  localStorage.setItem('siteLanguage', lang);

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    const key = element.getAttribute('data-i18n');
    if (key && labels[key]) {
      element.textContent = labels[key];
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((element) => {
    const key = element.getAttribute('data-i18n-placeholder');
    if (key && labels[key]) {
      element.placeholder = labels[key];
    }
  });

  const langBtn = document.getElementById('langBtn');
  if (langBtn) {
    langBtn.textContent = lang === 'es' ? 'EN' : 'ES';
    langBtn.title = lang === 'es' ? 'Switch to English' : 'Cambiar a español';
  }
}

function toggleLanguage() {
  const newLang = getCurrentLanguage() === 'en' ? 'es' : 'en';
  setLanguage(newLang);
}

function toggleSidebar() {
  const sidebar = document.getElementById('sidebar');
  sidebar.classList.toggle('active');
}

function scrollCarrusel(direction) {
  let left = null;
  if (direction === 'right') {
    left = 420;
  } else {
    left = -420;
  }
  const carrusel = document.getElementById('carrusel');
  carrusel.scrollBy({
    behavior: 'smooth',
    left: left
  });
}

window.addEventListener('DOMContentLoaded', () => {
  setLanguage(getCurrentLanguage());
});
